// 1. Arithmetic Operators
let x;

x = 5 + 5;
x = 5 - 5;
x = 5 * 5;
x = 5 / 5;
x = 10 % 8;

// Concatenation
x = 'Hello' + ' World';

// Exponent
x = 2 ** 3;

// Increment
x = 1;
// x = x + 1;
x++;

// Decrement
// x = x - 1;
x--;

// 2. Assignment Operators
x = 10;
x += 1;
x -= 1;
x *= 1;
x /= 1;
x %= 1;
x **= 1;

// 3. Comparison Operators

x = 2 == '2'; /* loose */
x = 2 === '2'; /* strict */
x = 2 != '2';
x = 2 !== '2';

x = 2 > 3;
x = 2 < 3;

console.log(x);
